    private void btn_salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_salvarActionPerformed
        try{
        modelcliente.setId_cliente(Integer.parseInt(this.c_cod.getText()));
        }catch(NumberFormatException e){  
        }
        modelcliente.setCli_nome(this.c_nome.getText());
        modelcliente.setCli_endereco(this.c_endereco.getText());
        modelcliente.setCli_bairro(this.c_bairro.getText());
        modelcliente.setCli_cidade(this.c_cidade.getText());
        modelcliente.setCli_uf(this.cb_uf.getSelectedItem().toString());
        modelcliente.setCli_cep(this.c_cep.getText());
        modelcliente.setCli_telefone(this.c_telefone.getText());
        
        if(SalvarAlterar.equals("Salvar")){
            if(controllerCliente.salvarClienteController(modelcliente)>0){
                JOptionPane.showMessageDialog(this, "Cliente Cadastrado Com Sucesso!", "PARABÉNS!", JOptionPane.INFORMATION_MESSAGE);
            //carregar os registros dos clientes cadastrados na tabela
                this.CarregarCliente();
                this.HabilitarDesabilitarCampos(false);
                this.LimparCampos();
            }else{
                JOptionPane.showMessageDialog(this, "Erro ao Cadastrar o Cliente!", "ERRO", JOptionPane.ERROR_MESSAGE);
            }
        }else{
            if(controllerCliente.atualizarClienteController(modelcliente)){
                JOptionPane.showMessageDialog(this, "Cliente Alterado Com Sucesso!", "PARABÉNS!", JOptionPane.INFORMATION_MESSAGE);
            //carregar os registros dos clientes cadastrados na tabela
                this.CarregarCliente();
                this.HabilitarDesabilitarCampos(false);
                this.LimparCampos();
            }else{
            JOptionPane.showMessageDialog(this, "Erro ao Alterar o registro do Cliente!", "ERRO", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btn_salvarActionPerformed

    private void btn_excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_excluirActionPerformed
        // excluindo um produto da tabela
        int linha = tbl_Cliente.getSelectedRow();
        int CodigoCliente = (int) tbl_Cliente.getValueAt(linha, 0);
        
        if (controllerCliente.excluirClienteController(CodigoCliente)){
            JOptionPane.showMessageDialog(this, "Cliente Excluído com Sucesso!", "PARABÉNS!", JOptionPane.INFORMATION_MESSAGE);
            this.CarregarCliente();
        }else{
            JOptionPane.showMessageDialog(this, "Erro ao Excluir Cliente!", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_excluirActionPerformed

    private void btn_alterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_alterarActionPerformed
        // TODO add your handling code here:
        this.HabilitarDesabilitarCampos(true);
        
        int linha = tbl_Cliente.getSelectedRow();
        int CodigoCliente = (int) tbl_Cliente.getValueAt(linha, 0);
        SalvarAlterar = "Alterar";
        
        modelcliente = controllerCliente.getClienteController(CodigoCliente);
        c_cod.setText(String.valueOf(modelcliente.getId_cliente()));
        c_nome.setText(modelcliente.getCli_nome());
        c_endereco.setText(modelcliente.getCli_endereco());
        c_bairro.setText(modelcliente.getCli_bairro());
        c_cidade.setText(modelcliente.getCli_cidade());
        cb_uf.setSelectedItem(modelcliente.getCli_uf());
        c_cep.setText(modelcliente.getCli_cep());
        c_telefone.setText(modelcliente.getCli_telefone());
    }//GEN-LAST:event_btn_alterarActionPerformed

