    private void HabilitarDesabilitarCampos(boolean condicao){
        c_nome.setEnabled(condicao);
        c_endereco.setEnabled(condicao);
        c_bairro.setEnabled(condicao);
        c_cidade.setEnabled(condicao);
        cb_uf.setEnabled(condicao);
        c_cep.setEnabled(condicao);
        c_telefone.setEnabled(condicao);
        btn_salvar.setEnabled(condicao);
    }
    
    //método que limpa os campos do formulário
    private void LimparCampos(){
        c_nome.setText("");
        c_endereco.setText("");
        c_bairro.setText("");
        c_cidade.setText("");
        c_cep.setText("");
        c_telefone.setText("");
    }
    
    private void CarregarCliente(){
        listaModelCliente = controllerCliente.getListaClienteController();
        DefaultTableModel modelo = (DefaultTableModel) tbl_Cliente.getModel();
        modelo.setNumRows(0);
        
        int cont = listaModelCliente.size();
        
        for(int i=0;i<cont;i++){
            modelo.addRow(new Object[]{
                listaModelCliente.get(i).getId_cliente(),
                listaModelCliente.get(i).getCli_nome(),
                listaModelCliente.get(i).getCli_cidade(),
                listaModelCliente.get(i).getCli_telefone(),
        });
        }
    }
    
